package org.gusdb.wdk.model;

public class WdkCookie {

  private String _name;
  private String _path;
  
  public String getName() {
    return _name;
  }
  public void setName(String name) {
    _name = name;
  }
  
  public String getPath() {
    return _path;
  }
  public void setPath(String path) {
    _path = path;
  }
  
}
